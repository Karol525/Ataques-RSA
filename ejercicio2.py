def exp_Mod(a, x, n):
    if x == 0:
        return 1
    elif x % 2 == 0:
        t = exp_Mod(a, x / 2, n)
        return (t * t) % n
    else:
        t = exp_Mod(a, x - 1, n)
        return (t * (a % n)) % n

